#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	int		i;
	t_list	*temp;

	i = 0;
	if (lst)
	{
		temp = lst;
		while (temp)
		{
			temp = temp->next;
			i++;
		}
	}
	return (i);
}
